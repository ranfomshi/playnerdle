var now = new Date();
var start = new Date(now.getFullYear(), 0, 0);
var diff = now - start;
var oneDay = 1000 * 60 * 60 * 24;
var day = Math.floor(diff / oneDay);
var state = 'initial'
var seconds = 00;
var tens = 00;
var globalSum = 0

function updateTimer() {

    var timer = new Date()
    h = 24 - timer.getUTCHours()
    m = 60 - timer.getUTCMinutes()
    s = 60 - timer.getUTCSeconds()
    timercontent = "<div class='counter'>" +
        (h).toString().padStart(2, 0) + "</div> <div class='counter'>" +
        (m).toString().padStart(2, 0) + "</div><div class='counter'>" +
        (s).toString().padStart(2, 0) + "</div>"
    $('#countdown').html(timercontent)

}
window.setInterval(updateTimer, 1000);

function initialise() {
    sendEvent("reaction_page", "page_load", "load")
    if (localStorage.getItem('introSeen') == null) {
        $('#introDiv').css('display', 'flex')
    }
    if (localStorage.getItem('historicScores') == null) {
        localStorage.setItem('historicScores', JSON.stringify([]))
    }
    var x = ''
    try { x = localStorage.getItem('lastPlayDay') } catch {}
    if (x == day) {
        loadDayState()
    } else {
        $('#stopBtn').css('display', 'none')
        $('#readyBtn').css('display', 'none')
        $('#shareBtn').css('display', 'none')
    }

}



