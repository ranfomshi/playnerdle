        //set last score to local storage
    var newScore = seconds + "." + tens
    if (localStorage.getItem('historicScores') == null) {
        localStorage.setItem('historicScores', '[]')
    }
    var oldScores = JSON.parse(localStorage.getItem('historicScores'))
    oldScores.push(newScore)
    localStorage.setItem('historicScores', JSON.stringify(oldScores))
    document.getElementById('mainDiv').style.background = 'black'
