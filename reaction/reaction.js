function shareClick() {
    $('#allResults').empty()
    $('#avgTime').empty()
    document.getElementById('shareDiv').style.display = 'flex'
    var sum = 0
        //add the scores from local storage to an on screen display (omit the comma if it's the last in the list)
    for (let i = 0; i < JSON.parse(localStorage.getItem('historicScores')).length; i++) {
        sum = sum + parseFloat(JSON.parse(localStorage.getItem('historicScores'))[i])
        if (i == JSON.parse(localStorage.getItem('historicScores')).length - 1) {
            $('#allResults').append('<div class="individualResult">' + parseFloat(JSON.parse(localStorage.getItem('historicScores'))[i]).toFixed(2) + 's</div>')
        } else {
            $('#allResults').append('<div class="individualResult">' + parseFloat(JSON.parse(localStorage.getItem('historicScores'))[i]).toFixed(2) + 's, ' + '</div>')
        }
    }
    globalSum = sum
    $('#avgTime').append('Your average: ' + (sum / JSON.parse(localStorage.getItem('historicScores')).length).toFixed(3) + 's')
    if (JSON.parse(localStorage.getItem('historicScores')).length > 12) {
        $('#allResults').css('font-size', 'smaller')
    }
    if (JSON.parse(localStorage.getItem('historicScores')).length > 24) {
        $('#allResults').css('font-size', 'xx-small')
    }
    sendEvent("reaction_page", "click", "share_click")
}

function rating(x) {
    if (parseFloat(x) < 0.3) {
        snackbar("WOW!!!")
    } else if (parseFloat(x) < 0.6) {
        snackbar("Not bad!!!")
    } else if (parseFloat(x) < 1) {
        snackbar("Hmm, could do better!!!")
    } else if (parseFloat(x) < 3) {
        snackbar("I'm ....lost for words! That was slowwwww")
    } else { snackbar("Did you forget to press?") }
}

function snackbar(x) {
    $('#snackbar').html(x)
        // Get the snackbar DIV
    var x = document.getElementById("snackbar");

    // Add the "show" class to DIV
    x.className = "show";

    // After 3 seconds, remove the show class from DIV
    setTimeout(function() { x.className = x.className.replace("show", ""); }, 3000);
}

function sendEvent(category, action, label) {
    if ("ga" in window) {
        tracker = ga.getAll()[0];
        if (tracker)
            tracker.send("event", category, action, label);
    }

}

